package model.bean;

/**
 *
 * @author henri
 */
public class Atrasados {
    private String nomeAtrasado;
    private String sobrenomeAtrasado;
    private String turmaAtrasado;
    private String horaAtrasado;
    private String dataAtrasado;

    public String getNomeAtrasado() {
        return nomeAtrasado;
    }

    public void setNomeAtrasado(String nomeAtrasado) {
        this.nomeAtrasado = nomeAtrasado;
    }

    public String getSobrenomeAtrasado() {
        return sobrenomeAtrasado;
    }

    public void setSobrenomeAtrasado(String sobrenomeAtrasado) {
        this.sobrenomeAtrasado = sobrenomeAtrasado;
    }

    public String getTurmaAtrasado() {
        return turmaAtrasado;
    }

    public void setTurmaAtrasado(String turmaAtrasado) {
        this.turmaAtrasado = turmaAtrasado;
    }

    public String getHoraAtrasado() {
        return horaAtrasado;
    }

    public void setHoraAtrasado(String horaAtrasado) {
        this.horaAtrasado = horaAtrasado;
    }

    public String getDataAtrasado() {
        return dataAtrasado;
    }

    public void setDataAtrasado(String dataAtrasado) {
        this.dataAtrasado = dataAtrasado;
    }
    
}
